#define Vew(v)     ((v)->el[3])
#define Vei(v,i)   ((v)->el[i])

#define VecX(v)     ((v)->el[0])
#define VecY(v)     ((v)->el[1])
#define VecZ(v)     ((v)->el[2])
#define VecW(v)     ((v)->el[3])
#define VecI(v,i)   ((v)->el[i])

#define VecEl(v)   ((v)->el)

#endif
